package main

import "fmt"

func main() {
	//String  can be added together +
	fmt.Println("Out" + "pit text")
	//Int and float
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	//booleans
	fmt.Println(true && false)
	fmt.Println(true || false) //boolean
	fmt.Println(!true)         //boolean
}
